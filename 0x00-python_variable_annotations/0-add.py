#!/usr/bin/env python3
"""
Module contains function called add, Sums two float values
"""


def add(a: float, b: float) -> float:
    """
    Adds two float values and returns the result

    Args:
    a (float): first float value.
    b (float): second float value.

    Returns:
        float: sum of a and b
    """
    return a + b